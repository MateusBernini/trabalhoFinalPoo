    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try{
        String q = txtQuantidade.getText().replace(",", ".");
        String v = txtValor.getText().replace(",", ".");       
        if (new FogaoController().edit(txtMarca.getText(), txtModelo.getText(), Double.valueOf(q), Double.valueOf(v), Integer.valueOf(txtQtdBocas.getText()), Double.valueOf(txtDesconto.getText()), Integer.valueOf(txtId.getText())))
            JOptionPane.showMessageDialog(this, "Informa√ß√µes editadas!!");
        }catch(IdInexistenteException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Erro ocorrido:" + e);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "N√£o √© permitido campos vazios !!\nPreencha todos os campos para editar !!");
            System.out.println("Erro ocorrido:" + e);
        } catch (ValorNuloException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Erro ocorrido:" + e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try{
        FogaoController fc = new FogaoController();
        String q = txtQuantidade.getText().replace(",", ".");
        String v = txtValor.getText().replace(",", ".");
              
        if(fc.create(txtMarca.getText(), txtModelo.getText(), Double.valueOf(q), Double.valueOf(v), Integer.valueOf(txtQtdBocas.getText()), Double.valueOf(txtDesconto.getText())))
            JOptionPane.showMessageDialog(this, "Dados do Fog√£o cadastrados !!");      
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Falha ao cadastrar !!\n" + "N√£o √© permitido campos em vazio !! " + "\nN√£o √© permitido Caracteres em valor e quantidade !!");
            System.out.println(e);
        } catch (ValorNuloException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Eroo ocorrido:" + e);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try{
        if (new FogaoController().delete(Integer.valueOf(txtId.getText()))){
            JOptionPane.showMessageDialog(this, "Dados do Fog√£o exclu√≠dos!!");             
        }           
        }catch(IdInexistenteException e){           
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Erro ocorrido:" + e);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Campo id vazio e/ou Inexistente !!\nDigite o Id que deseja Excluir !!" );
            System.out.println("Erro ocorrido:" + e);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

